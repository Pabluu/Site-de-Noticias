                <p>O município de Patrocínio, localizado no Triângulo Mineiro, terá que tomar uma série de medidas
                    para
                    combater
                    o trabalho infantil na cidade. A decisão é do juiz titular da Vara do Trabalho de Patrocínio,
                    Sérgio
                    Alexandre
                    Resende Nunes, nos autos da ação civil pública ajuizada pelo Ministério Público do Trabalho
                    contra o
                    município. Além disso, os julgadores da Sétima Turma do TRT mineiro, acompanhando o voto do
                    relator, juiz
                    convocado Márcio Toledo Gonçalves, condenaram o município ao pagamento de uma indenização por
                    danos morais
                    coletivos, no valor de R$ 500 mil.</p>

                <p>O órgão ministerial alegou que o Diagnóstico Intersetorial Municipal, elaborado pela OIT e pelo
                    Ministério
                    do
                    Desenvolvimento Social e Agrário, revelou dados preocupantes do município de Patrocínio, em
                    relação à
                    quantidade de casos de trabalho infantil e de crianças e adolescentes que não frequentam a
                    escola ou que
                    estão
                    ocupados no trabalho doméstico.</p>

                <p>Sustentou ainda que o município se recusou a firmar um termo de ajustamento de conduta (TAC), sob
                    o
                    argumento
                    de que <strong><em>“já executa ações estratégicas de combate ao trabalho
                            infantil”.</em></strong> Porém,
                    segundo o MPT, não provou a
                    adoção de políticas públicas de combate e erradicação do trabalho infantil.</p>

                <p>O órgão afirmou também que não viu alternativa, a não ser a propositura da ação civil pública,
                    <strong><em>“diante da
                            inércia do município de Patrocínio e do prefeito, aliada à inexistência de políticas
                            públicas
                            suficientes e
                            eficazes de prevenção e erradicação do trabalho infantil”.</em></strong> Por isso, além
                    das obrigações
                    de fazer, pediu a
                    condenação do ente público ao pagamento de indenização por danos morais coletivos.
                </p>

                <p><strong><em>Já o município apresentou defesa, arguindo a incompetência da Justiça do Trabalho
                            para julgar o
                            caso.</em></strong>e Alegou
                    ainda que o MPT tenta interferir na competência do Poder Executivo, na previsão orçamentária e
                    na
                    viabilidade
                    da gestão administrativa, e que não há, nos autos do processo, qualquer comprovação de que o
                    município de
                    Patrocínio estava consentindo com o trabalho de menores.</p>

                <p>Informou também que as atividades de combate e erradicação do trabalho infantil na cidade têm
                    sido
                    desenvolvidas dentro das possibilidades disponíveis para alcançar o maior número de usuários.
                    Por fim,
                    afirmou
                    que não houve dano moral coletivo, pois inexistiu transgressão a qualquer garantia ou direito
                    fundamental
                    coletivo.</p>

                <p>Mas, ao decidir o caso, o juiz esclareceu que é pacífico, na jurisprudência do Supremo Tribunal
                    Federal,
                    que,
                    “em situações excepcionais, o Poder Judiciário pode determinar que a administração pública adote
                    medidas
                    assecuratórias das políticas públicas previstas na Constituição, sem que isso configure violação
                    ao
                    princípio
                    da separação de poderes”.</p>

                <p>Conforme salientou o magistrado, a Constituição Federal proíbe expressamente o trabalho infantil,
                    erigindo,
                    desse modo, as políticas públicas de combate à exploração de crianças e adolescentes em condição
                    essencial
                    para a concretização da dignidade da pessoa humana. “Essa diretriz constitucional não pode,
                    evidentemente,
                    se
                    subordinar aos juízos ordinários de conveniência ou oportunidade. Por essa razão, configura
                    verdadeiro
                    limite
                    à discricionariedade do governo municipal, ao qual é vedado frustrar, com a sua inércia ou
                    omissão, a
                    decisão
                    do poder constituinte”.</p>

                <p>Por outro lado, destacou na decisão que a falta de recursos financeiros não pode também ser
                    invocada pelo
                    poder público como obstáculo à implementação das políticas públicas de envergadura
                    constitucional. No caso
                    dos
                    autos, o magistrado ressaltou que os próprios termos da contestação revelam que o município não
                    adota
                    políticas públicas de prevenção e erradicação do trabalho infantil, limitando-se, ao contrário,
                    “a realizar
                    o
                    trabalho de conscientização nas escolas e a oferecer atividades de lazer”.</p>

                <p>“A meu ver, as diversas obrigações de fazer postuladas na petição inicial revelam-se adequadas e
                    proporcionais
                    ao elevado propósito de combater o trabalho infantil, devendo, portanto, ser acolhidas”,
                    reforçou.</p>


                <p>Quanto ao pedido de indenização por dano moral coletivo, o juiz entendeu que inexiste comprovação
                    de que a
                    ausência de políticas públicas efetivas tenha causado danos à dignidade das crianças e
                    adolescentes do
                    município. Por isso, julgou improcedente esse pedido.</p>

                <p>Entretanto, os julgadores da Sétima Turma do TRT de Minas deram provimento parcial ao recurso do
                    MPT para
                    condenar o réu ao pagamento de reparação por dano moral coletivo, no valor de R$ 500 mil.</p>

                <p>De acordo com a decisão, a quantia deve ser revertida para projetos, órgãos públicos ou entidades
                    beneficentes
                    da região abrangida pela circunscrição da Vara do Trabalho de Patrocínio e/ou região da área de
                    atribuição
                    de
                    atuação da Procuradoria do Trabalho de Patos de Minas, Fundo Municipal da Infância e da
                    Adolescência (FIA),
                    ao
                    Fundo Especial do Ministério Público do Estado de Minas Gerais (Funemp), objetivando a
                    reconstituição dos
                    bens
                    lesados, com existência e atuação devidamente comprovadas no processo.</p>

                <p>O relator no processo, juiz convocado Márcio Toledo Gonçalves, frisou que é errônea a compreensão
                    de que o
                    trabalho infantil retiraria as crianças das ruas e da "malandragem". Na percepção do magistrado,
                    o trabalho
                    infantil, na verdade, gera um círculo vicioso de pobreza, uma vez que a criança pobre que
                    atualmente
                    trabalha
                    será um adulto mal-empregado ou até mesmo desempregado, pela falta de escolaridade e
                    profissionalização.
                    “Certo é que, em grande parte, o problema do trabalho infantil decorre da omissão do Estado
                    brasileiro -
                    União, Estados e Municípios – em estabelecer e adotar políticas públicas direcionadas à infância
                    e à
                    juventude”, finalizou.</p><br><br>


